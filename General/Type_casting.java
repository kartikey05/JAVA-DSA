import java.util.*;
public class Type_casting {
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        float d=sc.nextFloat();
        float a=25.25f;
        int b=(int)a;
        int c=(int)d;
        System.out.println((c));
        System.out.println((b));
    }
}
